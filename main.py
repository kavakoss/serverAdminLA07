def penambahan(a,b):
	total = a+b
	return (total)

def perkalian(a,b):
	total = a*b
	return (total)

def pengurangan(a,b):
	totalPengurangan = a-b
	return (totalPengurangan)

def bagi(a, b):
	hasil = a/b
	return (hasil)

